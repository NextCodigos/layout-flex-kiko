let contador = 0;
const N = 5;

do {
  contador++;
  console.log(`Iteración ${contador}`);
} while (contador < N);


// Este bucle siempre se ejecutará al menos una vez, independientemente de si la condición es verdadera o falsa.En este ejemplo, el bucle imprimirá el mensaje "Iteración 1" a "Iteración 5" en la consola.