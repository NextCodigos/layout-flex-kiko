// Puedes usar un while loop para crear un menú en javascript y dentro del while loop, puedes utilizar un switch statement para manejar las diferentes opciones del menú.Por ejemplo:

while (status !== 0) {
    // Mostrar menú
    switch (status) {
        case 1:
            // Hago A
            break;
        case 2:
            // Hago B
            break;
        case 3:
            // Hago C
            break;
        default:
            break;
    }
}

// En este ejemplo, el while loop se ejecutará mientras la variable "status" no sea igual a 0, y dentro del while loop, se mostrará el menú al usuario y se ejecutará la acción correspondiente según la opción seleccionada por el usuario(en este caso, 1, 2 o 3) en el switch statement.